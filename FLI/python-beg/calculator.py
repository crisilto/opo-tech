# 1. Crea un módulo llamado "calculator" que contenga funciones para sumar, restar, multiplicar
# y dividir dos números. Importa este módulo en otro archivo y usa sus funciones.

def sum(n1, n2):
    return n1+n2


def rest(n1, n2):
    return n1-n2


def multiply(n1, n2):
    return n1*n2


def divide(n1, n2):
    return n1/n2
